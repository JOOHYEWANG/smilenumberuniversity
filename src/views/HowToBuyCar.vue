        <h3>2. 100years later mathematics</h3>
        <p>
          집합은 수학에서 가장 기본적이며 필수 불가결한 도구이다. 그런데 만약 위와 같은 개념과 같이 100년 뒤의 문제는 아직 나타나지도 않았다. 즉, 그것을 우리는 우리의 어떤 직관으로도 분류 구분할 수 없다. 그러므로 아래와 같은 긴 시간의 구간을 가정할 때 전혀 다른 수학의 집합이 아닌 근본적인 무엇인가가 필요하다는 것을 예측 가능하다. 예전에 언급한 적이 있지만 미래의 수학을 우리가 조금 예측할 수 있다면 그것은 대부분 아주 복잡해서 단순화 불가능해 보이거나 또는 아예 수학적 언어를 찾을 수 없어서 그냥 놓아버리는 논리적 생각 들일 것이다. 왜냐하면 아직 수학적인 언어가 존재하지 않기 때문이다.
        </p>
        <p>
          그러므로 그러한 수학의 시작은 **공집합**에서부터 시작한다는 것은 명확하다. 이러한 수학의 시작으로 연속체 가설 논문에 소개된 확장된 **Excluded Middle**을 제안한다. 즉, 우리가 알 수 없는 비어 있는 논리와 참의 논리쌍 $(T, \emptyset)$의 공집합으로부터 정의되는 수학이 존재할 수 있다. 우리는 항상 수학적 논의의 시작을 그것이 비어있지 않다면부터 시작한다. 그러나 위와 같은 긴 구간을 가지는 시간대는 반드시 비어있는 부분이 존재한다. 위는 100년 뒤의 수학이 아니다. 그것을 어쩌면 조금 예측할 수도 있는 방법론적인 수학적 개념이다. 이러한 논리적 개념을 **상대적 공집합**이라고 정의하자. 왜냐하면 실제로는 비어 있지 않으나 시대에 따라서 어쩔 수 없이 미완성되어 있기 때문이다. 이러한 상대적 공집합의 직관적인 예제는 다음과 같다. 우리가 거울로 삼각형을 비추어 보면 그곳에는 또 다른 삼각형이 나타날 것이다. 그때 그 삼각형의 실체는 비어 있다. 그러나 그곳에는 또 다른 삼각형의 형태가 존재한다.
        </p>
        <p class="small-note"><sup>2</sup>즉, 삼각형은 미래에 있는 것이고 우리는 거울로 그것을 비추어서 볼 수 있는 방법이 존재할 수도 있다.</p>
        <p>
          우리는 이러한 직관적 이해의 개념으로부터 그것을 볼 수 있는 논리적 방법이 **수학적 대칭성**이라는 것을 또한 쉽게 추측할 수 있다.
        </p>
        <p>
          또 다른 좋은 예제로는 **허수 $i$의 존재성**이 있다. 이때 우리가 유클리드 공간의 모든 위치를 우리가 살고 있는 실제 세계의 위치와 대응 시킨다고 할 때 허수 $i$는 그러한 위치 어디에도 존재한다고 볼 수 없다. 즉, 그것의 실체는 존재하지 않는다. 그러나 그것의 수학적 논리는 존재하고 있다. 따라서 우리가 수학적 대칭성을 만족하는 거울을 만들 수 있다면 즉, 다시 말해서 그것이 시간의 흐름에 대하여 대칭성을 만족하고 그것의 실체가 현대 수학의 위치에 존재하지 않으나 수학적 논리는 존재할 때 그것을 먼 미래의 수학으로 간주할 수 있다.
        </p>
        <p>
          이러한 대칭적 개념을 사용할 수 있는 방법은 매우 간단하다. 거울을 다시 한번 거울로 비추어 보는 것이다. 그것이 다시 한번 거울 안에 그 형태가 나타날 때 즉, 그것이 또 한번 대칭성을 만족할 때 우리는 그것이 제한 조건을 만족한다는 것을 쉽게 알 수 있다. 우리는 실세계와 대응하는 좌표축을 종이 위에 그리자. 그리고 또한 복소수 축을 종이 위에 그리자. 그리고 실수 좌표축에서 복소수 좌표를 거울로 비추어 보면 허수 $i$는 마침내 실수 좌표계에 실체는 존재하지 않으나 그 형태는 나타날 것이다. 실제로 이러한 허수의 도입은 그동안 실수계에서 해결 불가능한 방정식들을 해결할 수 있도록 하였다.
        </p>
        <p>
          우리는 연속체 가설에서 확장된 EM에서 알 수 없는 부분을 공집합 처리함으로 원하는 결과를 얻을 수 있다. 그러나 그 알 수 없는 부분도 참인 것은 명확하다. 왜냐하면 둘은 논리적 동치이기 때문이다. 그리고 그것이 시간의 흐름에 대하여 다른 시대일 뿐이다. 그렇다면 우리는 앞과 같은 확장된 EM의 예제를 발견할 가능성이 존재한다면 또한 공집합 부분 또한 예측 가능할 것이다. 근본적인 문제로는 다음과 같은 물음이 있을 수 있다. **수학적 논리가 아직 존재하지 않는 공집합 영역에서 공집합을 정의해 보아라?** 역설적이게도 그것은 비어있다고 정의되지 않는다. 그러므로 그 상대적 공집합 안에도 수학이 존재한다고 볼 수 있다.
        </p>
        <p>
          우리가 100년 뒤의 수학을 알아야 할 필요가 있을까? 단순히 흥미에서? 아니다. 수학은 모든 과학의 언어로서의 근간 역할을 하고 있다. 만약 우리가 그것을 알 수 있다면 앞으로 세계에 나타날 문제들을 미리 예측하고 대비할 수도 있다는 것이다.
        </p>
        <p class="small-note"><sup>3</sup>이러한 예측은 수학의 언어로서의 역할은 필요에 의하여 발생한다고 볼 수도 있기 때문이다.</p>
        <p>
          예전에 이런 물음을 던진 적이 있다. 한 세대를 넘어서는 지식적 누적이 있어야 해결할 수 있는 수학적 문제가 존재할 수도 있다. 만약 그러한 문제가 반드시 미래에 발생한다면 미래인 즉, 우리의 자녀들의 세대 누구도 그것을 한 세대 안에 해결할 수 없을 것이다. 왜냐하면 그것은 미래 방향 시간으로 한 세대를 넘는 지식의 누적이 필요하기 때문이다. 하지만 우리가 만약 미리 그것을 내다볼 수 있는 방법이 존재한다면 그러한 세대를 넘어서는 문제들을 또한 해결 가능하다. 이러한 문제는 이미 존재하고 있는지도 모른다. 다른 우리의 지식적 누적의 양이 부족해서 그것을 알아차리지 못하고 있을 뿐일 수도 있다.
        </p>
        <p class="small-note"><sup>4</sup>성경에서는 엘로힘들이 사람이 불완전한 원죄를 지은 존재로서 자신들과 같이 될까 봐? 사람의 수명을 120년으로 제한하는 광경이 등장한다. 즉, 이러한 수학은 세대를 넘어선다. 그러므로 엘로힘들은 이러한 수학을 알 수도 있다. 그러므로 이러한 수학을 엘로힘 수학이라고 부르기로 한다. 그들의 그러한 언급으로부터 120년을 넘어서는 지식의 축적이 일어날 때만 알 수 있는 논리와 개념들이 존재한다는 것을 알 수 있다. 필자는 엘로힘들을 육체적 창조주로서 보고 있다. 그냥 혼자만의 생각일 뿐이다. 하하!</p>

        <h3>3. Eraser group</h3>
        <p>
          “벡터 공간이란 무엇인가?”(“what is the vector space”) 논문에 소개되어 있는 **지우개 군(eraser group)**은 위 조건을 만족하는 좋은 예이다. 즉, 다시 말해서 우리는 노트 필기라는 대상에 대하여 그것을 지우개 질 하는 것으로 그 수학적 개념을 표현하는 것이 가능하다. 즉, 또한 모든 노트 필기 가능한 수학적 논리는 지우개 질 가능하다. 그럼 우리는 실세계의 현실에 대하여 미래의 이미지의 변화를 지우개 질 되어 있다고 생각할 수도 있다. 그리고 변화를 현실이 지우개 질 되고 미래로 바뀐다고 간주할 수도 있다.
        </p>
        <p>
          대칭을 만족하는 상대적 공집합을 정의하는 방법은 간단하다. 지우개 군을 적용하면 된다. 시간이 흐르면 공간은 변화한다. 그렇다면 우리는 그 100년을 지우개 질 해보자. 즉, 그 위치에는 지우개 군(Eraser group)이 위치하는 것이다. 그때 우리는 100년 뒤를 비추어 볼 수 있다. 왜냐하면 $1 - 100$ 사이는 모두 지워졌기 때문이다. 이러한 개념이 수학적 거울 대칭의 이미지의 정의이다. 시공간의 특성은 비어 있는 공간에 이미지가 변화하는 것을 우리는 공간의 변화라고 느낀다. 따라서 그 모든 시간에 따른 이미지를 지우개 질 해버리면 그곳은 비어 있게 된다. 따라서 시간에 따라서 지우개 질 했으므로 0년의 위치에서 100년 뒤가 비추어 보인다. 사계절 미적분의 대수적 미적분은 **치환군**에 대하여 성립한다. 치환군은 모든 다른 군으로 변환 가능하다. 지우개 군을 사계절 미적분에 적용해서 그 지우개 질 된 상대적 공집합 공간 위치의 대칭의 특성으로 자리 잡을 수 있을 듯하다. 일반적으로 지우개 군을 적용했을 때 만족하면 그 수학, 과학은 더 이상 하는 게 아니다. 왜냐하면 사람이 다룰 수 있는 지적 한계를 넘어서기 때문이다. 사람의 수명은 120년 정도이고 그것을 넘어서는 지식의 누적으로부터 발생하는 논리는 다룰 수 없다고 보는 게 합당하다고 생각한다.
        </p>

        <h3>4. Icarus problem</h3>
        <p>
          우리는 1년부터 1만 년까지의 모든 시간의 변화를 주어진 매개변수에 대하여 앞장의 지우개 군을 적용해서 그 이미지를 지우는 것이 가능하다. 즉, 우리는 1만 년 뒤를 비추어 볼 수 있다. 그런데 이때 나타나는 것이 **이카루스 문제**이다. 이카루스 문제는 제한적인 삶과 제한적인 지적 능력을 가진 우리는 그것을 알고, 본다고 해도 알 수 없는 부분이 존재한다는 것이다. 즉, 우리가 주어진 매개변수 원소의 1년부터 1만 년 사이에 대하여 지우개 군을 적용했을 때 만족하면 그곳은 우리가 그 패턴을 알 수 있는 대칭이 비어 있다. 즉, 다시 말해서 우리는 그곳에서 알 수 있는 부분은 존재하지 않는다고 해석해야 한다.
        </p>
        <p>
          오늘의 생각 꺼리는 우리가 사는 세계에서 “패턴은 왜 존재하는가?”, “왜 존재해야 하는가?”이다. 우리는 패턴에 아름다움을 느낀다. 그리고 편안함도 느낀다. ‘왜 하필 그래야 하는 것일까?’ 패턴의 수학적 대표성은 그 예측 가능성에 있다. 즉, 우리는 그것에 패턴이 존재할 때 그것은 우리의 예측을 벗어나지 않는다는 것을 알 수 있다. 그러나 어떤 수학적 객체가 무작위적이라면 우리는 그것을 예측 불가능하다. 이러한 개념은 미분방정식에서는 그 해가 존재할 때이다. 그리고 함수를 정의하는 이유이기도 하다. 그 정의는 시간에 대하여 모든 위치의 변화를 우리가 원한다면 언제든지 알 수 있다는 의미이기도 하다. 사계절 미적분에서는 그 대칭성으로 수학적 패턴을 이야기한다.
        </p>
        <p>
          우리가 만약 무한한 지능과 지혜를 가진 존재였다면 어쩌면 딱히 특정 패턴이 존재하는 세계가 필요 없었을 수도 있다. 그러나 우리는 제한적인 지적 능력을 가지며 또한 120년 정도의 인생을 살 수 있을 뿐이다. 그러므로 패턴이 존재하는 세계는 제한적인 지적 능력 그리고 계산 능력이 만들어낸 하나의 수학적인 작품으로도 볼 수 있을 듯하다. 왜냐하면 우리는 그것에 패턴이 존재할 때 그것을 항상 우리의 예측 범위 안에 머무르게 할 수 있기 때문이다. 그러므로 패턴이란 제한적인 계산 능력으로 그것을 예측할 수 있는 최적의 수학적 방법인 것을 알 수 있다.
        </p>
        <p>
          좋은 예제로 만약 도서관 양의 학교에서의 움직이는 패턴을 모두 파악할 수 있다면 언제나 도서관에 앉아서 시간에 따라서 그녀의 움직이는 동선을 모두 파악할 수 있다는 의미이다. 앞의 문제에 대하여 다음과 같은 물음들이 있을 수 있다.
        </p>
        <p><strong>5과제:</strong> 오늘의 문제이다. 앞의 논리적 개념을 만족하는 수학적 패턴을 공리적 방법으로 정의하여라!</p>

        <p>
          **Problem:** 우리가 다루는 모든 현대적 도구들에 수학적 패턴을 주는 것이 가능할까?
        </p>
        <p>
          **Proof:** 패턴의 대표성은 예측 가능성에 있다. 우리는 120년의 제한적인 삶을 산다. 그런데 현재의 원인이 120년을 넘어서 결과가 발생하는 사건들은 아주 많다. 그러므로 그것은 우리의 예측 가능한 범위를 넘어선다. 따라서 이 세계에서 모든 것을 패턴을 주어서 고안하는 것은 불 가능하다. $\blacksquare$
        </p>
        <p>
          위의 증명은 “왜 우리가 모든 경제학적 재난, 기후적 재난, 환경적 재난을 예측하는 것은 불가능한가?”에 대한 대답이 될 것이다. 그러므로 또한 기후적 재난은 예방하는 것이지 그것을 컨트롤하는 것은 불가능하다는 것을 입증하기도 한다. 왜냐하면 그것은 이카루스 문제이다. 즉, 우리의 지적 한계를 넘어서므로 우리는 그것의 해결 방법이 존재해도 제대로 바르게 이해할 수 없다. 경제학적 재난과 예측 불가능성 또한 이카루스 문제인 것을 알 수 있다. 즉, 그것은 예방하는 것이지 어떤 방법론적 해결 방법이 존재하지 않는다. 이러한 이카루스 문제의 판별식은 앞장의 지우개 군을 적용했을 때 알 수 있다.
        </p>
        <p>
          즉, 간단하다. 지우개로 1년부터 1만 년까지의 모든 변화를 지워보자. 즉, 지우개 군이 작동했을 때 $1 - 10000$년 사이에는 지우개 군 대칭이 정의된다. 지우개 군 대칭이 이야기하는 것은 우리가 알 수 없는 것이 대칭을 이룬다는 것이다.(알 수 없도록 지워진 것이다.) 즉, 알 수 있는 것도 대칭을 이루듯 알 수 없는 것도 수학적 패턴을 가진다. 우리는 그것에 패턴이 존재해도 그것이 비어있으면 인식할 수 없으므로 알 수 없다는 것이 타당한 결론이다. 그러므로 간단히 지우개 군을 적용했을 때 만족하면 우리가 알 수 있는 패턴인 대칭성 부분이 비어 있는 것이다. 즉, 우리가 그곳에서 알 수 있는 것은 없다고 해석해야 한다.
        </p>

        <h4>4.1. Example 1. Environments problem</h4>
        <p>
          연세대학교 미래캠퍼스 도서관의 책장의 형태는 각각이 모두 동일하다. 즉, 그것은 대칭성을 가진다. 지우개 군이 이야기하는 것은 그 책장 중 하나가 비어 버리면 우리는 그 책장과 대응하는 지식을 알 수 없다는 것이다. 즉, 다시 말해서 지우개 군을 작동시키면 우리는 플라스틱이 썩는 데 걸리는 시간 120년이라는 지식이 지워진다는 것을 알 수 있다. 우리는 플라스틱 그리고 비닐봉투를 무분별하게 사용하고 그대로 매립하는 것이 120년 뒤의 환경에 어떤 영향을 만들어 낼지 명확히 알 수 없다. 따라서 이것은 우리의 120년이라는 지식의 한계를 넘는 이카루스 문제이다. 논의의 단순화를 위하여 우리는 스티로폼, 플라스틱, 비닐봉투가 썩는 데 소요되는 시간이 모두 120년이라고 가정하자. 앞은 지우개 군을 만족한다. 즉, 120년 뒤가 그대로 지금 시대에 대칭적으로 비추어진다. 왜냐하면 직관적으로 그것을 모두 환경에서 지워 버렸을 때 120년 뒤의 깨끗한 환경을 명확하게 알 수 있다. 엄밀한 증명은 각각이 ‘벡터 공간이란 무엇인가?’ 문서를 보고 적용해 보기를 바란다. 그냥 지우개 질 하면서 군 공리를 헤아려 보면 명확하다.
        </p>
        <p>
          간단히 서술해보면 썩는 데 소요되는 시간이 120년이다. 그러므로 그 원소에 대하여 120년을 지워보면 된다. 물품이 썩는 데 소요되는 시간을 지워보면 그 물품은 아직 생산되지 않았다고도 볼 수 있다. 왜냐하면 사용되지 않았기 때문이다. 그러므로 교환법칙을 만족한다. 그때 지우개 질 했을 때 비어 있으므로 당연히 항등법칙을 만족한다.(항등 성질을 만족할 때의 조건이다. 직관적으로 우리가 실세계에서 사용하는 물품에 대하여 그 덧셈의 항등원은 공집합 비슷한 상태를 만들어 내면 된다. 물론 앞은 직관적 이해이다. 엄밀하게는 주어진 조건에 따른 정보, 지식으로 정의해야 한다.) 그에 대하여 역원 성질을 만족하는 것은 역시 그 물품을 사용하지 않을 때 정의된다. 즉, 그것은 비어 있다. 왜냐하면 사용하지 않으면 그 앞 원소는 지우개 질 되기 때문이다. ($120 - 120 = 0$)
        </p>
        <p class="small-note"><sup>6</sup>엄밀한 증명은 ‘지우개 군의 원소들이 군의 원소라고 할 수 있는가?’ 정도의 물음이 있을 수 있다. 그런데 원소는 그 물품이 가진 주어진 조건의 지식에 대하여 정의되므로 문제가 발생하지는 않을 듯하다.</p>

        <h4>4.2. Example 2. Economic inflation problem</h4>
        <p>
          120년 뒤의 경제적 상황을 생각하자. 물론 $1 - 120$년까지 당연히 경제학자들이 예측하지 못한 많은 경제적 재난이 발생할 것이다. 그럼 이때 사이의 모든 경제적 재난을 지우개 질 해보자. 그때 우리는 120년 뒤를 엄밀하게 예측 가능하다. 왜냐하면 모든 예측 불가능성이 지워졌기 때문이다. 즉, 지우개 군의 직관적인 조건을 만족한다. 앞의 경제적 재난의 한 종류인 **인플레이션 사태**를 생각하자. 그것은 간단히 물품을 구입하려는 사람이 물품보다 아주 많을 때 발생한다. 즉, 화폐의 가치는 폭락한다. 이때 상품의 개수가 기준보다 적다는 것은 그것을 지우개 질 했다는 것과 논리적 동치이다. 그러므로 지우개 군의 조건으로 적용하자. 아주 단순화해서 생수를 사려는 사람이 100명인데 물품은 0개이면 그 가격은 끝도 없이 올라가고 그것이 화폐의 가치 폭락이다.
        </p>
        <p>
          그럼 지우개 군을 만족함을 보이자. 물품의 개수를 지우개 질 할 때 그것은 당연히 교환법칙을 만족한다. 어떤 생수를 먼저 지우건 동일한 것은 당연하다. (물품의 지식이 물품의 개수이다.) 다음 항등 특성은 덧셈에 대하여 $g + 0 = g$를 만족한다. 왜냐하면 물품, 물품의 지식을 지우면 $0 = 0$이다. 역원 특성은 $g - g = 0$이다. 즉, 상품의 개수는 제한 조건인 지식이 지우개 질 된 것과 일치한다. 따라서 화폐 폭락은 지우개 군의 조건을 만족한다. 따라서 우리가 그 사건으로부터 알 수 있는, 예측할 수 있는 것은 없다. 화폐 가치 폭락 즉, 경제학적 인플레이션 사태는 그 후유증이 120년을 넘어갈 수도 있다. 즉, 그 사태를 맞은 국가는 다시 경제학적으로 재건하지 못할 수도 있다. 그러므로 위의 조건이 적절하다는 것을 알 수 있다.(아주 단순화시켜서 적용한 경우이다. 재난이 또 다른 재난과 예측 불가능성을 연역적으로 만든다고 볼 수도 있다.) 이때 또한 예제 3(Example 3)인 기후적 재난의 하나의 예인 태풍에 대하여 위의 인플레이션 사태와 거의 비슷하게 약속할 때 지우개 군을 만족한다.
        </p>
        <p>
          위 예제들에서 알 수 있듯이 이카루스 문제는 재난이 이미 발생하면 수습할 방법이 없다.(방법이 존재해도 우리는 그것을 이해할 수도 없다.) 그러므로 그 예방이 최선이라는 것을 추측 가능하다.
        </p>

        <h4>4.3. Example 3. Imaginary number $i$</h4>
        <p>
          “우리는 존재하지 않는 것의 변화량이나 넓이를 알 수 있을까?” 당연히 질문에 대한 답은 “알 수 없다.”이다. 삼차원 실공간을 우리가 살고 있는 이 세계의 모든 위치와 대응 시킬 때 간단히 허수 $i$는 그 위치가 비어 있다. 즉, 다시 말해서 우리는 비어있는 $\emptyset$ 수학적 위치로부터 수학을 구축했으며 그것의 미분적분인 변화량과 넓이까지도 계산하여 구하고 있다. 이러한 사실이 가능한 이유는 다음과 같다. 먼저 미분방정식에서 그것을 적분할 수 있는 이유를 생각하자. 미분방정식은 적어도 하나의 미분연산자를 포함하고 있다. 따라서 당연히 그것은 미적분학의 기본법칙, 그리고 항등식의 성질에 의하여 적분 가능하다. 이러한 개념을 허수로 확장하면 그것은 기존 공간과 결합할 때 그것은 위치 표현을 가지며 또한 변화량과 그 넓이까지 계산 가능하다는 것을 쉽게 알 수 있다. 그것이 바로 **복소수(complex number)**이다. 복소 평면은 이차원 실공간과 일대일 대응 가능하다. 즉, 서로 위치를 바꾸어 쓰는 것이 가능하다. 즉, 그것은 벡터로도 또한 표현 가능하므로 우리가 가시적으로 느낄 수 있는 수학적 표현도 가지고 있다. 그러므로 직관적으로 그 위치에 대한 미적분이 존재할 수 있다는 것을 쉽게 추측 가능하다. 우리는 이러한 기존 공간과의 결합으로부터 허수라는 상상 속의 수를 현실 세계로 끄집어 내었다. 그리고 또한 앞장에 언급한 것과 같이 공집합 $\emptyset$으로부터 정의 가능한 수학이 존재한다는 것을 쉽게 확인 가능하다.
        </p>

        <h3>5. Mathematics from Empty Set $\emptyset$</h3>
        <p>
          수학적 표현과 논리 $L$(logic)에 대하여 생각해보자. ‘우리는 왜 수학을 기하학과 같은 대상으로 표현할까?’ 우리가 어떤 수학 정리의 증명을 시도했을 때 사용된 논리를 간단히 $L$이라고 부르기로 하자. $L := \bigcup L_i$로 정의 가능하다. 우리는 먼저 우리의 수학적 생각이라는 것을 정의하기로 한다. 그것을 간단히 수학적 생각 $T$(thinking)라고 나타내기로 하자. 분명히 수학적 논리는 수학적 사유로부터 발생하므로 $T \subset L$이 성립한다. 그리고 그러한 수학적 표현은 또한 우리의 수학적 사유를 포함하므로 $L \subset T$가 성립한다.
        </p>
        <p>
          수학적 사유를 하며 가끔은 다음의 경험을 했을 것이다. “우리가 하는 수학의 증명에는 진짜 모순이 없을까?” 우리는 러셀의 역설 그리고 위 정의에 의하여 우리의 수학적 사유 그리고 표현되는 수학적 개념을 보고 동시에 그것을 판단, 판별하는 것은 불가능하다는 것을 쉽게 알 수 있다. 간단히 직관적으로 표현하면 우리는 우리가 수학적 생각을 하는 동안은 그 생각을 하고 있지 동시에 ‘그 생각하는 것 자체가 참인가?’라는 생각을 동시에 할 수 없는 것은 당연하다. 왜냐하면 우리는 한 번에 하나의 생각만을 할 수 있기 때문이다. 즉, 그것을 할 수 있다고 가정할 때 러셀의 역설이 발생한다. 현대 수학은 위를 공집합 처리하고 그 위에 집합론을 바탕, 기반으로 하는 수학을 구축하였다. 그러므로 당연히 힐베르트가 언급했던 그 수학은 불가능하다. 즉, 힐베르트 프로그램과 동치로 괴델의 정리에서 이야기하던 바로 그 참이지만 증명 불가능 명제가 존재하게 된 것이다. 우리에게 수학적 논리가 참인 것을 판단할 수 있는 방법(즉, 그 판별을 이야기한다.)이 존재하게 된다면 당연히 기계적 알고리즘으로 그것을 구축하는 것도 가능할 것이다. 그런데 그 자체로 러셀의 역설이 발생하고 우리는 이미 그것을 수학에서 배제하였다. 그러므로 괴델의 결과는 당연한 것이다. 우리는 참, 참, 참,...으로만 수학을 구축했기 때문이다. 즉, 그 논의 안에는 아예 괴델이 불완전성 정리에서 이야기한 그 참이지만 증명 불가능한 명제가 존재하지도 않기 때문이다.
        </p>
        <p>
          앞장에서 우리는 그것을 $(T, \emptyset)$의 쌍으로 표현하였다. 구조적으로 $T$ 그리고 $\emptyset$는 일대일 대응 시키는 구조를 주는 것이 언제나 가능하다. 왜냐하면 우리는 비어있는 집합을 기존의 것 즉, $T$와 결합시키는 것만으로 그것을 표현 가능하다. 그럼 이제부터 간단하다. $T$와 공집합 $\emptyset$의 자리를 바꾸어 쓰기만 하면 된다. 엄밀한 증명은 모든 $T$집합에 공집합을 포함시킬 때 가능하다. 이것은 위치 공집합 허수로 복소수를 구축한 그 방법과 동일한 알고리즘을 가진다는 것을 쉽게 추측 가능하다. 그러므로 그것은 일대일 대응 가능하고 우리는 그것으로부터 공집합 자리의 괴델이 불완전성 정리에서 말하던 그 참이지만 증명 불가능 수학들을 수학의 현실 세계로 끄집어낼 수 있다는 것을 알 수 있다. 그러므로 괴델의 유명 결과는 러셀의 역설이 발생 가능한 수학적 구조를 수학에서 함부로 엄밀한 증명 없는 논의에 의하여 배제함으로 만들어졌다는 것을 알 수 있다. 직관적 이해는 간단하다. (참, 참)인 상태는 간단히 참이다. 그런데 (참, 공집합)이라는 상태를 우리가 받아들일 때 수학을 하면서 동시에 그것이 참인가라는 질문을 마침내 던질 수 있다는 것이다. 물론 방법적으로는 이미 소개한 작은 세계 수학을 사용해야 한다.
        </p>

        <h4>5.1. Example 4. Russell’s paradox</h4>
        <p>
          현대 수학의 사상(명제, 증명, 정리, 정의, 개념, 숫자, 연산,...) 대부분을 공집합으로 일단 보내보자. 그 방법은 대수적 체 공리(algebraic field axiom)를 공집합으로 보내는 것이다. 그때 많은 공간, 명제, 정리, 증명의 계산 그리고 연산, 대칭성 등등등,... 수학의 수많은 부분이 공집합 처리된다. 우리는 숫자가 주어진다고 해도 간단한 연산도 수행하는 것이 불가능하다. 즉, 참인 명제가 주어진다고 해도 우리는 증명 불가능하다. 그것이 괴델의 불완전성 정리의 증명 구조이다. $a \cdot b = \emptyset$(정수 덧셈 군을 생각하자.)에서 그는 명제를 공집합 부분으로 보낸 것이다. 이때 증명도 당연히 비어져 있었다. 따라서 명제 자리는 공집합이 나타난다. 즉, 다시 말해서 서로 자리를 교환하였다. 그러므로 이때 기존 현대 수학은 대수적 체를 공집합으로 보내는 것으로 비워졌다. 그럼 러셀의 역설을 생각하자. 아래와 같이 나타난다.
        </p>
        <p class="math-display">
          $S = \{A \mid A \notin A\}$
        </p>
        <p class="math-display">
          (i). $S \in S \implies S \notin S$ (ii). $S \notin S \implies S \in S$
        </p>
        <p>
          대수적 체 공리의 군 공리 부분을 생각하자. 그것은 덧셈에 대하여 군을 이룬다. 그런데 이제는 군을 이루지 않아도 그것은 참이다. 즉, 그것은 대칭적이지 않아도 참이다. EM에서 증명의 대칭성이란? 다음과 같이 약속 가능하다. 즉, 그것이 참이고 동시에 거짓일 때 그것은 대칭적이지 않다고 정의된다. 왜냐하면 참을 대칭적 공간에 위치시킬 때 그것이 참이라는 것은 또 다른 형태가 참으로 다른 위치에 대칭적으로 나타난다는 것을 의미한다. 또한 물론 대칭성이 사라지면 숫자의 그 고유 특성도 사라진다. 왜냐하면 우리는 대칭성, 공통의 성향을 추출하여 숫자라는 개념을 발견하였기 때문이다. 그럼 간단히 정수와 대응 시키자.(명제는 양의 정수, 증명은 음의 정수이다.) 즉, 현대 수학의 모든 명제의 개수는 셀 수 있다. 그때 위의 러셀의 역설은 그 가운데 어디에도 없다. 즉, 그곳에는 군 공리를 만족하는 어떤 숫자도 대응 시킬 수 없다. 즉, 그것은 비워져 있는 숫자이다. 이때 위 언급한 것과 같은 정수 덧셈 군의 대칭성을 생각하자. 그런데 그것은 이미 공집합 처리되었다. 즉, 모든 수학의 명제와 증명은 공집합 처리되었다. 우리는 이때 명제를 증명 가능할 때 그것을 셀 수 있다고 약속할 때 당연히 러셀의 역설을 만족하는 명제는 셀 수 없었다. 즉, 그것은 서로 부정으로 정의되었다. 처음 위와 같은 명제 증명의 셀 수 있다는 증명 특성이 공집합 처리되었다. 그러므로 그곳에는 더 이상 셀 수 있는 것이 참이 아니다. 따라서 이때 러셀의 역설을 만족하는 셀 수 없던 그 명제는 참으로 나타나게 된다.
        </p>
        <p>
          연속체 가설 문서의 작은 세계 수학을 생각해보자. 우리는 위에서 수학의 명제 그리고 증명을 정수와 대응 시켰다. 그때 명제는 양의 정수이고 그에 대하여 증명은 음의 정수이다. 우리는 수학의 명제와 증명이 참의 특성을 추출하자. 즉, 모든 참인 명제는 그것이 존재할 때 셀 수(알 수 있다.) 있으므로 모두가 논리 값 참에 대하여 동치이다. 그런데 괴델 정리의 특성을 만족하는 명제의 경우 참이라고 해도 그것의 증명이 불가능하다. 이것은 작은 세계 수학의 정의와 일치한다. 그러므로 그것은 동치인데 우리가 알 수 없을 뿐이다. 따라서 그것의 논리쌍은 동치이고 $(T, \emptyset)$으로 나타난다. 그러므로 연속체 가설 문서의 EM 확대 정리에 의하여 그것은 참이다. 이것으로 위 러셀의 역설의 참 값은 명확하게 보여졌다고 생각한다.
        </p>
        <p class="small-note"><sup>7</sup>수학이므로 증명은 당연히 정의만 적용하면 끝이다.</p>
        <p>
          우리는 수학의 명제와 증명을 정수의 셀 수 있는 성질과 대응 시켰다. 그런데 괴델 정리의 특성을 만족하는 명제의 경우 증명이 존재하지 않는다. 따라서 그 역원이 정의되지 않는다. 따라서 그것은 정수의 군 공리 또한 만족하지 않는다. 따라서 그곳에는 대칭성이 없으므로 그 숫자는 기존의 숫자의 성향을 추출하는 방법으로 구축할 수 없으므로 셀 수 없다.
        </p>
        <p class="small-note"><sup>8</sup>이때 숫자의 성향이란 배 5개, 사과 5개, 오렌지 5개의 공통된 특성인 5개를 의미한다.</p>

        <h3>6. Proof of the Empty $\emptyset$ Mathematics</h3>
        <p>
          앞장에서는 EM을 사용하여서 새로운 수학을 구축할 수 있다고 주장하였다.
        </p>
        <p>
          **정리 1:** Excluded middle을 사용하여 수학을 구축 가능하다.
        </p>
        <p>
          **증명:** 우리는 참 또는 거짓 중 하나의 부분만을 사용할 것이다. 수학적 참 그리고 거짓은 종속적인가? 독립적인가? 그것을 참을 정의할 때 그 논리적 부정을 거짓으로 약속하면 그것은 종속적이다.(vice versa,...) 그러나 우리는 그것이 참이거나 거짓일 때 굳이 다른 한쪽을 동시에 다루지 않는다. 즉, 그것은 독립적이다. 그러므로 EM에 위배되는 수학 개념의 경우 참이거나 거짓 그중 한쪽만을 다룰 때 그것은 수학적으로 오류가 없다.
        </p>
        <p>
          간단하게 참 그리고 거짓을 동시에 만족하면 둘 중 하나만 만족하는 경우로 약속하면 된다. 그리고 그것을 그러한 방식으로 우회해서 계속 이론을 전개하면 된다.
        </p>
        <p>
          **정리 2:** 참이나 거짓을 한쪽만 다룰 때 EM이 바이패스 된다.
        </p>
        <p>
          **증명:** 그것이 종속적이라면 참 그리고 거짓은 서로 관계가 있다고 약속되므로 서로의 연역되는 정리에 영향을 만들 것이다. 그러나 독립일 때 그러한 참이나 거짓 중 한쪽을 비어있다고 약속하면 그러한 영향은 만들지 않는다. 그러한 EM에 위배될 때 수학적 참이나 거짓 중 하나를 지우는 방법은 단순하다. 지우개 질하면 된다. 즉, 지우개 군을 적용하면 된다. 그때 우회된다.
        </p>
        <p>
          그러한 참이나 거짓을 지우개 질 하는 방법은 굉장히 단순하다. 그러한 개념의 대칭성을 변경하면 된다. 그때 그래프 위에서 생각해보면 EM이 우회되는 것을 쉽게 상상 가능하다. 대칭성을 변경하고 지우개 질 한다고 참이나 거짓이 변화할까? 참 그리고 거짓의 종속성의 관계를 해제하는 것과 마찬가지이다. 그것이 참인 경우 정 n각형 위에 그리는 것이 가능하다고 가정할 때 그 부정은 거짓이다. 즉, 거짓의 부정은 참이다. 따라서 대칭성이 변경되면 둘의 종속성은 사라진다.
        </p>
        <p>
          간단하게는 수학적 증명 또는 주어진 수학적 개념의 참 또는 거짓을 확인하는 절차는 그것의 확인에 여러 가지 수학적 개념이 결합된다. 우리는 그러한 개념들 $1, \dots, n$까지 각각이 증명에 $1/n$의 역할을 한다고 약속하자. 앞과 같이 약속할 때 EM을 만족하는 개념들은 모두 대칭을 이룬다고 이야기할 수 있다. 왜냐하면 마치 확률적 사건이 발생하지 않은 것과 같이 그것의 참 또는 거짓이 결정되지 않은 상태이기 때문이다. 그러므로 그것은 발생하지 않은 확률적 사건과 같이 간주 가능하고 따라서 그것을 정보의 양으로 해석 가능하다. 이때 우리는 참 또는 거짓 중 하나의 경로만이 수학적 오류가 없다고 약속하자. 우리는 거짓인 경우를 다루기로 했다고 가정하고, 정 n각형 위에서 참값을 지우는 과정을 생각하자. 그때 대칭성은 모두 지우개 질 된다. 즉, 참과 거짓의 종속성이 사라지므로 그것은 EM에 위배되지 않는다. 참값을 만든 증명에 사용된 논리적 개념들을 모두 오류가 있다고 간주하는 것이 거짓 값으로부터 만들어지는 수학적 이론의 제한 조건이다.
        </p>
