          <strong>Abstract.</strong> 웃는숫자 자동차 플랫폼 사용 비용은 부동산의 전세와 월세 개념을 도입할 예정이다. 그리고 앞장의 포인트 경제학을 적용할 것이다. 즉, 보증금을 또한 투자비용으로 볼 수 있다. 그러므로 가상세계에 새로운 부동산 시장을 만드는 것이다. 즉, 부동산 가치의 증가와 하락(판매량, 홍보량) 등으로 포인트를 부여할 것이다. 그리고 그러한 포인트를 관련업계에서 사용 가능하도록 한다면 시너지 효과까지 만들어질 듯하다. 간단히 플랫폼의 그 업체의 자리를 부동산과 같이 임대하고 그 자리는 마치 예전 닷컴 회사들이 지분을 매각하듯 다른 사람에게 양도할 수 있는 방법까지 구축할 것이다. 즉, 권리 포인트도 받을 수 있다. 그때 온라인 가상세계에 새로운 부동산 시장이 만들어지고, 새로운 투자도 일어날 수 있다고 생각한다. 즉, 수학적인 포인트화폐세계의 시작이다. 물론 여러 다른 자동차 관련 업계와도 협업해야 포인트를 여러 곳에서 사용할 수 있을 것이다. 이러한 개념은 부동산 거품을 잡는 새로운 가상 세계의 부동산 시장이다. 즉, 다시 말해서 가상 세계의 추상적인 자동차 도로이다. 앞과 같이 가상세계에 추상적인 자동차 도로, 즉 새로운 경제적 가치를 구축해 나가야 새로운 포인트화폐의 가치가 만들어질 것이다. 그리고 포인트 부동산 세계에서 아파트나 건축의 개념은 포인트를 사용할 수 있도록 계약하는 협업 업체에 그분들이 사용할 수 있는 또 다른 플랫폼을 제공하는 것으로 그것을 구축할 수 있다고 생각한다. 그러한 작업은 많은 아이디어가 새롭게 필요하며 많은 업체가 또 다시 그곳에 입주 가능하다. 이러한 포인트 부동산의 아파트의 건설은 많은 아이디어 그리고 전문인력 그리고 많은 시간이 소요될 것이다.
