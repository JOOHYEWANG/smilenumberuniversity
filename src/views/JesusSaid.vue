     <section class="section">
        <h3>Fractal Geometry and Artificial Intelligence</h3>

        <p>요즘 물리학의 진짜 핵심은 실세계에서 실험할 수 없는 경우이다. 양자역학 고양이라는 가상 세계의 사고 실험을 제안하기도 한다. 이러한 경우 수학에서는 컴퓨터 계산으로 만들어 낼 수 있는 프렉탈 기하학이 있다. 그러한 프렉탈 기하학은 자연에서 혼돈계를 나타내고 있다고 한다. 따라서 그러한 무작위 혼돈계에서 물리학 실험을 할 수 없는 것은 자명하다.</p>

        <p>집합론의 핵심개념은 칸토어의 대각논법(Diagonal argument)이다. 즉, 무한을 세는 수학적 방법이 발견되었다.</p>

        <ul>
          <li>($i$) 우리가 명확하게 구분할 수 있는 (셀 수 있는 경우를 의미한다.) 색상(Color)은 몇 가지인가? 그리고 각각의 C₁,...,Cₙ까지 분명히 그것을 확대하거나 하면 셀 수 있다. (유리수 기수)</li><br>

          <li>($ii$) 이때 우리가 셀 수 없는 (실수기수)는 어디인가? 대각논법 사용해서 도출 가능할 듯 하다. 그리고 반복 패턴의 크기는 적당히 함수로 표현하면 된다.</li><br>

          <li>($iii$) 결국 프렉탈은 우리가 알 수 없는 부분을 명확하게 구분 (수학적으로는 분류 작업이라고 한다.)하는 기하학으로 생각할 수도 있다.</li><br>
        </ul>

        <p>앞으로 해야 할 수학 공부는 위의 절차를 일반화하고 추상화할 수 있을 듯 하다. 그리고 단순화시켜서 나타내야겠다. 저러한 프렉탈을 생성하는 함수 그리고 일반화, 추상화된 절차의 함수(우리가 알 수 있는 부분을 의미한다)를 대각논법을 사용하여 적용 혹은 함수화한다. 다시 말해서 "알 수 없는 부분을 도출 가능한가?" (마지막으로 처음 프렉탈 기하학의 생성함수와 비교해 본다.)</p>

      

        <p>추측(conjecture): 수학적으로 우리는 컴퓨터가 사고하는 방식을 완전하게 이해하는 것은 불가능하다. 현재 발전하고 있는 인공지능의 경우를 생각하면 명확하다. 우리는 그러한 컴퓨터 과학 분야도 아직 제대로 이해하지 못하고 있다. 그런데 그 기반인 컴퓨터의 계산 과정, 메모리 과정을 이해할 수 없는 것도 자명하다.</p>

        <p>관심 있는 순수 수학 문제로 우회했다 (transformation). 적어도 하나의 예제만 증명되어도 모든 프렉탈 기하학에 적용하는 것이 가능하다. 이러한 증명 방법은 반증(Mathematics proof disprovement)이라 한다. 하나의 그런 프렉탈 기하학이 존재해도 우리는 모든 경우 또한 알 수 없다고 가정해야 한다. 왜냐하면 과학 분야에 응용된다고 가정했을 때, 그것이 언제 나타날지 알 수 없기 때문이다.</p>

        <p>따라서 아래의 정리가 완전하게 증명되기 전까지는 인공지능(AI)을 소프트웨어와 가상공간의 도구 정도로 제한해야 할 수도 있다.</p>

        <p>특히 일반화하여 증명하면, 산업혁명, 정보화 혁명, 인공지능 혁명 등 시대별 누적된 문제를 해결할 수 있다. 산업혁명 시대에는 "먹고 사는 문제"만이 중요시되어 인간성이 상실되기도 했다. 정보화 혁명 시대에는 지나친 정보의 유입으로 인해 사람들은 서로 소통이 되지 않는 문제들이 발생하고 있다. 가짜 뉴스, 조기 성인 영상 노출 등도 문제다. 결국 시대적 배경을 악용하는 자들도 생겨났다.</p>

        <p>갈루아의 5차 방정식 근의 공식 존재 불가 증명은 그 시대적 특수 사례이며, 괴델의 정리는 "참이지만 증명 불가한 명제"의 존재를 보여준다. 결국 우리는 바른 것, 틀린 것 모두를 명확히 알 수 없는 세상에 살고 있다. 리만 가설은 시대의 흐름에 따라 변하는 수학 문제의 양상을 보여주고 있으며, 그것이 "세상에서 가장 어려운 문제"라 불리는 이유다.</p>

        <p>이제 인공지능 혁명 시대가 오고 있다. 많은 수학자들이 인공지능을 공부하고 있다. 그런데 수학은 하지 않고 인공지능만 하는 이유는 무엇일까? 이제 위 추측의 증명에 대하여 생각해보자.</p>

        <h3>정리(theorem) 1</h3>
        <p>수학적으로 우리는 컴퓨터가 사고(계산 방법 그리고 램, 하드의 메모리 기억 방식)하는 방식을 완전하게 이해하는 것은 불가능하다.</p>

        <h4>증명(proof):</h4>
        <p>위에 의하여 컴퓨터의 사고(계산 및 메모리 구조)에는 괴델의 불완전성 정리에 의해 우리가 알 수 없는 부분이 분명히 존재하고 있다.</p>

        <h3>정리 2</h3>
        <p>모든 수학의 역방향 증명에 대하여 슬라이딩 퍼즐 게임의 역시간 구조를 적용 가능하다.</p>

        <h4>증명:</h4>
        <p>수학의 모든 명제와 증명의 발견은 어떤 확률적 사건인 것이 자명하다. 즉, 그 발견에 어떤 확률값이 존재한다. 따라서 임의의 모든 수학적 명제와 증명에는 확률적 사건의 원인(reason)과 결과(conclusion)를 순서로 정의할 수 있으며, 따라서 슬라이딩 퍼즐 게임의 역시간 구조도 정의할 수 있다.</p>

        <p>수학 정리, 명제의 함의(implication) 표현도 앞의 논의가 성립한다. 영국 수학자 하디(Hardy)는 리만 가설의 역방향 증명을 시도하였다.</p>

        <p>공리(axiom): 시간은 흐른다.</p>

        <p>어떤 기기를 발견하고 만들더라도 우리가 그 메커니즘과 알고리즘을 모두 알 수는 없다. 이 노트의 내용은 크게 신경 쓰지 않아도 되며, 하나님 아버지께 감사드린다. 아멘. 프렉탈 기하학을 읽는 함수는 Chat-GPT가 제공했다. 두 번째 논문이다. 아치형 곡선을 GPT 계단 함수 공식으로 표현하면 층을 가지게 되며, 그것이 바로 프렉탈 기하학을 읽는 하나의 방법이다.</p>

